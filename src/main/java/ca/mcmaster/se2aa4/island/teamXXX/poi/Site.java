package ca.mcmaster.se2aa4.island.teamXXX.poi;

public class Site {

    private int x;
    private int y;
    private String id;

    public Site(int x, int y, String id) {
        this.x = x;
        this.y = y;
        this.id = id;
    }

    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }

    public String getId() {
        return id;
    }

}